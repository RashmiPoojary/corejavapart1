package corejavapart1;

public class IncrementDecrement {

	public static void main(String[] args) {
		int a=6;
		int b=71;
		int c=45;
		int d=67;
		int n1=++a;
		System.out.println("The pre-increment is="+n1);
		int n2=b++;
		System.out.println("The post-increment is="+n2);
		int n3=--c;
		System.out.println("The pre-decrement is="+n3);
		int n4=d--;
		System.out.println("The post-decrement is="+n4);
		

	}

}
